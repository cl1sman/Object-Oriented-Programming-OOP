"""
    Como são identificadas as variáveis.
"""

"""
    Inteiro
        byte = inteiro com sinal (1 byte)
        short = inteiro com sinal (2 bytes)
        int = inteiro com sinal (4 bytes)
        long = inteiro com sinal (8 bytes)

    Ponto Flutuante
        float = ponto flutuante(4 bytes)
        double = ponto flutuante(8 bytes)

    Char = caractere(2 bytes)
        Caracteres Especiais
        \’ --> apóstrofo
        \” --> aspas
        \\--> barra invertida
        \b --> backspace
        \n --> nova linha
        \t --> tabulação

    Boolean = true ou false(1 bit)
    String = Uma sequência de caracteres

    Valores literais
        boolena -> true/false
        int -> exemplo: 100
        float -> exemplo: 1.5F
        double -> exemplo: 1.5
        char -> exemplo: 'a'
        string -> exemplo: "a..a", null, ""
"""